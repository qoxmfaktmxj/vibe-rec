create table recruit.interview (
    id                  bigint generated by default as identity primary key,
    application_id      bigint not null references recruit.application(id) on delete cascade,
    job_posting_step_id bigint not null references recruit.job_posting_step(id) on delete cascade,
    scheduled_at        timestamptz,
    status              varchar(20) not null default 'SCHEDULED',
    note                text,
    created_at          timestamptz not null default current_timestamp,
    updated_at          timestamptz not null default current_timestamp,
    constraint interview_status_ck check (status in ('SCHEDULED', 'COMPLETED', 'CANCELLED', 'NO_SHOW'))
);

create unique index interview_app_step_uidx
    on recruit.interview(application_id, job_posting_step_id);

create table recruit.evaluation (
    id              bigint generated by default as identity primary key,
    interview_id    bigint not null references recruit.interview(id) on delete cascade,
    evaluator_id    bigint not null references platform.admin_account(id),
    score           smallint check (score between 1 and 5),
    comment         text,
    result          varchar(20) not null default 'PENDING',
    created_at      timestamptz not null default current_timestamp,
    updated_at      timestamptz not null default current_timestamp,
    constraint evaluation_result_ck check (result in ('PENDING', 'PASS', 'FAIL', 'HOLD'))
);

create unique index evaluation_interview_evaluator_uidx
    on recruit.evaluation(interview_id, evaluator_id);
